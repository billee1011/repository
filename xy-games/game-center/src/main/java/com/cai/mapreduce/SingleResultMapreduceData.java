package com.cai.mapreduce;

/**
 * 单个结果集的Mapreduce返回
 * @author chansonyan
 * 2018年6月19日
 */
public class SingleResultMapreduceData {
	
	private int id;
	private double value;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public double getValue() {
		return value;
	}
	public void setValue(double value) {
		this.value = value;
	}
	
	
}
